namespace TrickyMottram.UnitConversion.Time.Enums
{
    /// <summary>
    /// Enumeration of supported time units for conversion.
    /// </summary>
    public enum TimeUnit
    {
        /// <summary>
        /// Nanoseconds (ns)
        /// </summary>
        Nanosecond,

        /// <summary>
        /// Microseconds (µs)
        /// </summary>
        Microsecond,

        /// <summary>
        /// Milliseconds (ms)
        /// </summary>
        Millisecond,

        /// <summary>
        /// Seconds (s)
        /// </summary>
        Second,

        /// <summary>
        /// Minutes (min)
        /// </summary>
        Minute,

        /// <summary>
        /// Hours (h)
        /// </summary>
        Hour,

        /// <summary>
        /// Days (d)
        /// </summary>
        Day,

        /// <summary>
        /// Weeks (wk)
        /// </summary>
        Week,

        /// <summary>
        /// Months (mo) — average month duration (30.44 days)
        /// </summary>
        Month,

        /// <summary>
        /// Years (yr) — average year duration (365.25 days)
        /// </summary>
        Year
    }
}
