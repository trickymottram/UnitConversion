namespace TrickyMottram.UnitConversion.Weight.Enums
{
    /// <summary>
    /// Represents supported units of weight and mass.
    /// </summary>
    public enum WeightUnit
    {
        /// <summary>
        /// Milligram (mg) — one thousandth of a gram.
        /// </summary>
        Milligram,

        /// <summary>
        /// Gram (g) — base unit in the metric system.
        /// </summary>
        Gram,

        /// <summary>
        /// Kilogram (kg) — 1,000 grams.
        /// </summary>
        Kilogram,

        /// <summary>
        /// Tonne (t or metric ton) — 1,000 kilograms.
        /// </summary>
        Tonne,

        /// <summary>
        /// Ounce (oz) — commonly used in the imperial and US customary systems.
        /// </summary>
        Ounce,

        /// <summary>
        /// Pound (lb) — 16 ounces.
        /// </summary>
        Pound,

        /// <summary>
        /// Stone (st) — used in the UK, 14 pounds.
        /// </summary>
        Stone,

        /// <summary>
        /// Hundredweight (cwt) — used for bulk goods, UK: 112 lb, US: 100 lb (this enum uses UK definition).
        /// </summary>
        Hundredweight,

        /// <summary>
        /// US Ton (short ton) — 2,000 pounds.
        /// </summary>
        UsTon,

        /// <summary>
        /// UK Ton (long ton) — 2,240 pounds.
        /// </summary>
        UkTon
    }
}
